; =============================================================================
; [S2d] CUSTOM-SKILL RECORD FORK  (de-aliased custom skills, ids $DE-$FF)
; -----------------------------------------------------------------------------
; The three record-table indexer sites (LoadB54_5249 $5251, LoadB54_526e $5276,
; CacheSkillRecordFields_5298 $529E) each did `ld hl,$4013 / add hl,bc / add hl,bc`
; (index the 222-entry SkillRecordPtrTable by skill id). A custom id (>= $DE)
; overshoots that table. Each site is now `call Fork54_RecordIndex / nop / nop`
; (byte-neutral 5-for-5). Fork54 returns HL = &record-pointer-entry, exactly like
; the original, but for id >= $DE it indexes CustomRecordPtrTable instead.
; This single fork fixes targeting (+2), magnitude (+11..), battle MP-cost (+4),
; status (+5), ai_weight (+3) AND the enemy AI (shared record reader) at once.
; bc(=id, b normally 0) is preserved; readers don't reuse bc afterwards.
; =============================================================================
Fork54_RecordIndex:
    ld a, c
    cp $DE
    jr nc, .custom
    ; vanilla path: identical to the original 3 instructions
    ld hl, $4013
    add hl, bc
    add hl, bc
    ret
.custom:
    push bc
    sub $DE                 ; index = id - $DE
    add a                   ; *2
    ld c, a
    ld b, $00
    ld hl, CustomRecordPtrTable
    add hl, bc
    pop bc
    ret

CustomRecordPtrTable:       ; indexed by (id - $DE)*2
    dw $41CF                ; [$DE] retired POC -> Blaze record (benign default)
    dw $41CF                ; [$DF] retired POC -> Blaze record (benign default)
    dw CustomRecord_E0      ; [$E0] MagicBurn

; --- MagicBurn ($E0) 19-byte record ------------------------------------------
; Based on BigBang/MegaMagic-class all-foes magic, with mp_cost (+4) zeroed (the
; handler charges the real half-MP) AND +9 (anim9, cached to $dcff) = $17 instead
; of MegaMagic's $02. $dcff bit4/bit2 GATE the announce+animate presentation phase
; (bank $52 $6F5B / $7E08, both target-driven, not skill-id keyed) -- MegaMagic's
; $02 leaves those bits clear, which is why copying it gave instant silent damage.
; $17 matches every announcing/animating damage spell (Bang/Bolt/Lightning/BigBang).
; +0=$46 keeps MegaMagic's effect-class -> announcement msg $40 "uses all magic powers!".
;   +0 effect_class=$46  +1 category=$12  +2 target=$12(ALL FOES)  +3 ai=0  +4 mp=0
;   +5 status=$10  +6 dmg_class=$05  +7 flags7=$01  +8 field8=$06  +9 anim9=$17
