# CLI
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(description='Generate GBC palette attribute data')
    parser.add_argument('layout_json', nargs='?', help='Custom layout JSON file (20×16 grid)')
    parser.add_argument('--tileset', default='29:01',
                        help='Tileset key "bank:step" (default: 29:01 = Farm)')
    parser.add_argument('--label', default='CustomAttr_Room6B',
                        help='ASM label for the compressed data')
    parser.add_argument('--dump-mapping', metavar='TS',
                        help='Dump tile→palette map for a tileset and exit')
    parser.add_argument('--verify', action='store_true',
                        help='Verify roundtrip decompression')
    parser.add_argument('-o', '--output', help='Output file (default: stdout)')
    args = parser.parse_args()
    
    rom = read_rom()
    
    if args.dump_mapping:
        tile_pal, sources = build_tile_palette_map(rom, args.dump_mapping)
        print(f'Tileset {args.dump_mapping}: {len(tile_pal)} tiles mapped from {len(sources)} screens')
        for s in sources:
            print(f'  {s}')
        print(f'\nTile → Palette:')
        for t in sorted(tile_pal.keys()):
            print(f'  0x{t:02X} ({t:3d}) → palette {tile_pal[t]}')
        return
    
    if not args.layout_json:
        parser.error('layout_json required (or use --dump-mapping)')
    
    # Load custom layout
    with open(args.layout_json) as f:
        data = json.load(f)
    
    # Handle both raw array and {layout: ...} wrapper
    if isinstance(data, dict):
        layout = data.get('layout', data)
    else:
        layout = data
    
    if len(layout) != 16 or any(len(r) != 20 for r in layout):
        print(f'ERROR: Layout must be 20×16, got {len(layout[0])}×{len(layout)}',
              file=sys.stderr)
        sys.exit(1)
    
    # Build tile→palette mapping
    tile_pal, sources = build_tile_palette_map(rom, args.tileset)
    print(f'Tileset {args.tileset}: {len(tile_pal)} tile→palette mappings '
          f'from {len(sources)} screens', file=sys.stderr)
    
    # Generate attr data
    attr_raw = generate_attr_data(layout, tile_pal)
    print(f'Generated 256-byte attr map', file=sys.stderr)
    
    # Compress
    compressed = compress_lz(attr_raw)
    print(f'Compressed: {len(compressed)} bytes '
          f'({len(compressed)/256*100:.0f}% of original)', file=sys.stderr)
    
    # Verify roundtrip
    if args.verify:
        # Write temp data to verify decompression
        marker = compressed[2]
        out_len = compressed[0] | (compressed[1] << 8)
        # Use our own decompressor
        decompressed = _decompress_verify(compressed)
        if decompressed == attr_raw:
            print('Roundtrip verification: PASS', file=sys.stderr)
        else:
            print('Roundtrip verification: FAIL', file=sys.stderr)
            diffs = sum(1 for a, b in zip(decompressed, attr_raw) if a != b)
            print(f'  {diffs} byte differences', file=sys.stderr)
    
    # Output
    asm = format_asm_db(compressed, args.label)
    if args.output:
        with open(args.output, 'w') as f:
            f.write(asm + '\n')
        print(f'Written to {args.output}', file=sys.stderr)
    else:
        print(asm)


def _decompress_verify(compressed):
    """Simple LZSS decompressor for roundtrip verification."""
    out_len = compressed[0] | (compressed[1] << 8)
    marker = compressed[2]
    src = 3
    output = bytearray(out_len)
    out_pos = 0
    
    while out_pos < out_len and src < len(compressed):
        b = compressed[src]
        src += 1
        
        if b != marker:
            output[out_pos] = b
            out_pos += 1
        else:
            if src + 1 >= len(compressed):
                break
            offset_lo = compressed[src]
            src += 1
            control = compressed[src]
            src += 1
            
            offset_hi = (control >> 4) & 0x0F
            copy_len_code = control & 0x0F
            
            offset = offset_lo | (offset_hi << 8)
            
            if copy_len_code == 0x0F:
                if src >= len(compressed):
                    break
                copy_len = compressed[src] + 19
                src += 1
            else:
                copy_len = copy_len_code + 4
            
            for j in range(copy_len):
                if offset + j < len(output) and out_pos < out_len:
                    output[out_pos] = output[offset + j]
                    out_pos += 1
    
    return bytes(output[:out_len])


if __name__ == '__main__':
    main()
