#!/usr/bin/env python3
"""Generate GBC palette attribute data for DWM1 custom room layouts.

The GBC BG attribute map assigns a palette (0-7) to each tile position.
In vanilla rooms, this data is stored compressed in bank $3C/$3D, indexed
by attr_idx from bank $17's per-room/per-screen step entries.

Format (256 bytes decompressed):
  8 rows × 32 bytes. Each row covers 2 tile rows (even + odd).
  Each 32-byte row: [10 bytes for even row][6 padding][10 bytes for odd row][6 padding]
  Each byte is nibble-packed: high nibble = left tile's palette, low nibble = right tile's palette.

This tool:
  1. Builds a tile→palette mapping from vanilla rooms using a given tileset
  2. Generates the 256-byte attr map for a custom layout
  3. LZSS compresses it
  4. Outputs ASM db statements for bank $64

Usage:
  python3 generate_attr_map.py layout.json                 # generate for Farm tileset
  python3 generate_attr_map.py layout.json --tileset 29:01 # explicit tileset
  python3 generate_attr_map.py --dump-mapping 29:01        # show tile→palette map
  python3 generate_attr_map.py --all-tilesets               # dump all tileset mappings

Generated by: tools/generate_attr_map.py
"""
import json, os, sys, argparse, re

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
ROOT_DIR = os.path.join(SCRIPT_DIR, '..')
ROM_PATH = os.path.join(ROOT_DIR, 'data', 'DWM-original.gbc')

sys.path.insert(0, ROOT_DIR)
from tools.compress_tiles import compress_lz
from tools.decompress_tiles import decompress_lz


# ---------------------------------------------------------------------------
# ROM data extraction
# ---------------------------------------------------------------------------

def read_rom():
    with open(ROM_PATH, 'rb') as f:
        return f.read()


def get_attr_ptr_table(rom):
    """Read the AttrPtrTable from bank $17 ($476F): 107 entries × 2 bytes."""
    base = 0x17 * 0x4000
    table_addr = 0x476F
    entries = []
    for i in range(107):  # mapIDs 0-106 ($00-$6A)
        off = base + table_addr - 0x4000 + i * 2
        ptr = rom[off] | (rom[off + 1] << 8)
        entries.append(ptr)
    return entries


def parse_room_attr_entry(rom, ptr):
    """Parse a room's attribute screen table and step entries from bank $17.
    
    Returns: dict mapping screen_index → list of (attr_idx, attr_bank, pal_ptr) per step
    """
    base = 0x17 * 0x4000
    off = base + ptr - 0x4000
    
    # Read screen pointers until we detect data section
    # Screen table: pairs of 2-byte pointers, $FFFF = unused screen
    # We read up to 8 screen slots (max screens per room)
    screen_ptrs = []
    for i in range(8):
        p = rom[off + i * 2] | (rom[off + i * 2 + 1] << 8)
        screen_ptrs.append(p)
    
    result = {}
    for scr_idx, scr_ptr in enumerate(screen_ptrs):
        if scr_ptr == 0xFFFF:
            continue
        
        scr_off = base + scr_ptr - 0x4000
        # Format: [wram_addr:2] then [attr_idx:1, attr_bank:1, pal_ptr:2] × N steps
        wram = rom[scr_off] | (rom[scr_off + 1] << 8)
        if not (0xD900 <= wram <= 0xD9FF):
            continue
        
        steps = []
        pos = scr_off + 2
        for step_num in range(10):  # max steps
            attr_idx = rom[pos]
            attr_bank = rom[pos + 1]
            pal_lo = rom[pos + 2]
            pal_hi = rom[pos + 3]
            
            # Validity check: attr_bank should be in tileset range
            if attr_bank < 0x20 or attr_bank > 0x50:
                # Check if this looks like next screen's WRAM address
                maybe_wram = attr_idx | (attr_bank << 8)
                if 0xD900 <= maybe_wram <= 0xD9FF:
                    break
                break
            
            pal_ptr = pal_lo | (pal_hi << 8)
            steps.append((attr_idx, attr_bank, pal_ptr))
            pos += 4
        
        if steps:
            result[scr_idx] = steps
    
    return result


