def load_editor_rooms():
    """Extract room data from the HTML tile editor."""
    editor_path = os.path.join(ROOT_DIR, 'towards_editor', 'DWM1_Tile_Editor.html')
    if not os.path.exists(editor_path):
        return {}
    
    with open(editor_path) as f:
        html = f.read()
    
    rooms = {}
    # Find all room entries like "4_0":{...}
    pattern = re.compile(r'"(\d+_\d+)"\s*:\s*\{')
    for m in pattern.finditer(html):
        key = m.group(1)
        start = m.end() - 1  # opening brace
        depth = 0
        for i in range(start, min(start + 10000, len(html))):
            if html[i] == '{':
                depth += 1
            elif html[i] == '}':
                depth -= 1
            if depth == 0:
                try:
                    room = json.loads(html[start:i + 1])
                    rooms[key] = room
                except json.JSONDecodeError:
                    pass
                break
    
    return rooms


