# Tile → Palette mapping
# ---------------------------------------------------------------------------

def build_tile_palette_map(rom, tileset_key):
    """Build a tile_index → palette_number mapping for a given tileset.
    
    Analyzes all vanilla rooms that use this tileset by cross-referencing
    editor room data (for layouts + tileset assignments) with ROM attr data.
    
    Args:
        rom: full ROM bytes
        tileset_key: "bank:step" like "29:01" (Farm tileset)
    
    Returns:
        dict mapping tile_index (0-255) → palette_number (0-7)
    """
    rooms = load_editor_rooms()
    attr_table = get_attr_ptr_table(rom)
    
    tile_pal = {}
    conflicts = []
    sources = []
    
    for room_key, room in rooms.items():
        if room.get('ts') != tileset_key:
            continue
        
        mapid = room['mapid']
        scr = room['scr']
        layout = room['layout']
        
        if mapid >= len(attr_table):
            continue
        
        # Get attr data for this room/screen
        room_attr_ptr = attr_table[mapid]
        if room_attr_ptr == 0xFFFF:
            continue
        
        screen_steps = parse_room_attr_entry(rom, room_attr_ptr)
        if scr not in screen_steps:
            continue
        
        # Use step 0's attr data
        attr_idx, attr_bank, pal_ptr = screen_steps[scr][0]
        
        result = decompress_lz(rom, attr_bank, attr_idx)
        if not result or len(result[0]) != 256:
            continue
        
        attr_data = result[0]
        
        # Extract tile→palette pairs
        screen_count = 0
        for attr_row in range(8):
            for half in range(2):
                tile_row = attr_row * 2 + half
                base = attr_row * 32 + half * 16
                for col_pair in range(10):
                    byte_val = attr_data[base + col_pair]
                    left_pal = (byte_val >> 4) & 0xF
                    right_pal = byte_val & 0xF
                    
                    left_tile = layout[tile_row][col_pair * 2]
                    right_tile = layout[tile_row][col_pair * 2 + 1]
                    
                    for tile, pal in [(left_tile, left_pal), (right_tile, right_pal)]:
                        if tile in tile_pal:
                            if tile_pal[tile] != pal:
                                conflicts.append(
                                    f'Tile 0x{tile:02X}: pal {tile_pal[tile]} vs {pal} '
                                    f'(room {room_key})')
                        else:
                            tile_pal[tile] = pal
                            screen_count += 1
        
        sources.append(f'{room_key} ({room.get("name", "?")}): +{screen_count} tiles')
    
    if conflicts:
        print(f'WARNING: {len(conflicts)} palette conflicts:', file=sys.stderr)
        for c in conflicts[:10]:
            print(f'  {c}', file=sys.stderr)
    
    return tile_pal, sources


def generate_attr_data(layout_20x16, tile_pal, default_pal=0):
    """Generate the 256-byte attribute map for a custom layout.
    
    Args:
        layout_20x16: list of 16 rows, each 20 tile indices
        tile_pal: dict mapping tile_index → palette_number
        default_pal: palette for unmapped tiles (default 0)
    
    Returns:
        bytes: 256-byte attribute map in game format
    """
    output = bytearray(256)
    unmapped = set()
    
    for attr_row in range(8):
        for half in range(2):
            tile_row = attr_row * 2 + half
            base = attr_row * 32 + half * 16
            
            for col_pair in range(10):
                left_col = col_pair * 2
                right_col = col_pair * 2 + 1
                
                left_tile = layout_20x16[tile_row][left_col]
                right_tile = layout_20x16[tile_row][right_col]
                
                left_pal = tile_pal.get(left_tile, default_pal)
                right_pal = tile_pal.get(right_tile, default_pal)
                
                if left_tile not in tile_pal:
                    unmapped.add(left_tile)
                if right_tile not in tile_pal:
                    unmapped.add(right_tile)
                
                output[base + col_pair] = (left_pal << 4) | right_pal
            
            # Padding bytes 10-15 stay as 0x00
    
    if unmapped:
        print(f'WARNING: {len(unmapped)} tiles without palette mapping '
              f'(using default {default_pal}): {sorted(unmapped)}', file=sys.stderr)
    
    return bytes(output)


def format_asm_db(data, label, bytes_per_line=16):
    """Format compressed data as ASM db statements."""
    lines = [f'; {label}: {len(data)} bytes compressed (256 decompressed)']
    lines.append(f'{label}:')
    for i in range(0, len(data), bytes_per_line):
        chunk = data[i:i + bytes_per_line]
        hex_bytes = ', '.join(f'${b:02X}' for b in chunk)
        lines.append(f'    db {hex_bytes}')
    return '\n'.join(lines)


