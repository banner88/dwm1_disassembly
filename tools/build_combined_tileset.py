def get_palette_colors(rom, ts_key):
    """Get the 8-palette color set (64 bytes) for a tileset.

    For ROM tilesets: looks up the tileset's mapID via $26DD, then reads
    pal_ptr from bank $17's attr step entry.
    For EXT: tilesets: reads from the extracted palette JSON.

    Returns: dict mapping pal_number (0-7) → list of 4 RGB15 values (each 2 bytes)
    """
    if ts_key.startswith('EXT:'):
        # External tileset — read palette from extracted JSON
        ext_name = ts_key.split(':')[1]
        pal_json = os.path.join(ROOT_DIR, 'extracted', 'custom_tilesets',
                                f'{ext_name}_palettes.json')
        if not os.path.exists(pal_json):
            raise ValueError(f"External tileset {ts_key}: palette file not found")
        import json as _json
        with open(pal_json) as f:
            pal_data = _json.load(f)
        colors = {}
        # Use per-tile exact colors: for each palette group, collect ALL colors
        # used by tiles in that group and pick the 4 most representative
        if 'tiles' in pal_data:
            from collections import Counter as _Counter
            group_colors = {}  # group → list of all tile color sets
            for tidx_str, tinfo in pal_data['tiles'].items():
                g = tinfo.get('pal_group', 0)
                if g not in group_colors:
                    group_colors[g] = _Counter()
                for c in tinfo['colors']:
                    group_colors[g][tuple(c)] += 1
            for group, color_freq in group_colors.items():
                # Take the 4 most common colors in this group
                top4 = [c for c, _ in color_freq.most_common(4)]
                while len(top4) < 4:
                    top4.append(top4[-1] if top4 else (0, 0, 0))
                # Sort by luminance (lightest first)
                top4.sort(key=lambda c: c[0]*0.299+c[1]*0.587+c[2]*0.114, reverse=True)
                pal_colors = []
                for r, g_val, b in top4:
                    gr, gg, gb = min(31, r//8), min(31, g_val//8), min(31, b//8)
                    val = (gr & 0x1F) | ((gg & 0x1F) << 5) | ((gb & 0x1F) << 10)
                    pal_colors.append((val & 0xFF, (val >> 8) & 0xFF))
                colors[group] = pal_colors
        else:
            # Fallback to old group-average format
            for group_str, rgb_list in pal_data.get('palette_colors', {}).items():
                group = int(group_str)
                pal_colors = []
                for r, g_val, b in rgb_list:
                    val = (min(31,r) & 0x1F) | ((min(31,g_val) & 0x1F) << 5) | ((min(31,b) & 0x1F) << 10)
                    pal_colors.append((val & 0xFF, (val >> 8) & 0xFF))
                colors[group] = pal_colors
        for i in range(8):
            if i not in colors:
                colors[i] = [(0, 0)] * 4
        return colors, 0
    bank, gfx_step = parse_ts_key(ts_key)

    # Find which mapID uses this bank+step in the $26DD table
    mapid = None
    for mid in range(107):
        off = 0x26DD + mid * 8
        if rom[off] == gfx_step and rom[off + 1] == bank:
            mapid = mid
            break

    if mapid is None:
        raise ValueError(f"No mapID found for tileset {ts_key} (bank ${bank:02X} step ${gfx_step:02X})")

    # Read pal_ptr from bank $17's AttrPtrTable
    base17 = 0x17 * 0x4000
    attr_table_addr = 0x476F
    ptr_off = base17 + attr_table_addr - 0x4000 + mapid * 2
    room_ptr = rom[ptr_off] | (rom[ptr_off + 1] << 8)

    if room_ptr == 0xFFFF:
        raise ValueError(f"MapID ${mapid:02X} has no attr entry")

    # Navigate to screen 0, step 0's pal_ptr
    scr_off = base17 + room_ptr - 0x4000
    scr0_ptr = rom[scr_off] | (rom[scr_off + 1] << 8)
    if scr0_ptr == 0xFFFF or scr0_ptr < 0x4000:
        raise ValueError(f"MapID ${mapid:02X} screen 0 invalid")

    step_off = base17 + scr0_ptr - 0x4000
    # Skip 2-byte WRAM addr
    step_off += 2
    # Step entry: [attr_idx:1, attr_bank:1, pal_ptr_lo:1, pal_ptr_hi:1]
    pal_ptr = rom[step_off + 2] | (rom[step_off + 3] << 8)

    # Read 64 bytes of palette color data from bank $17
    pal_off = base17 + pal_ptr - 0x4000
    colors = {}
    for pal in range(8):
        pal_colors = []
        for c in range(4):
            lo = rom[pal_off + pal * 8 + c * 2]
            hi = rom[pal_off + pal * 8 + c * 2 + 1]
            pal_colors.append((lo, hi))
        colors[pal] = pal_colors

    return colors, pal_ptr


