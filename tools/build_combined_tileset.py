#!/usr/bin/env python3
"""Build a combined tileset from a multi-tileset editor export.

Takes the JSON export from DWM1_Multi_Tileset_Editor and produces:
  - bank_067.asm: Combined GFX data (cherry-picked 2bpp tiles, LZSS compressed)
  - Updated bank_064.asm: Layout + attr data for the combined room
  - palette_colors.inc: 64 bytes of merged palette color data for bank $17

The editor export format:
  {
    "palette": [{"slot":0, "ts":"29:01", "idx":42, "pal":3, "walkable":true}, ...],
    "layout": [[0, 1, 2, ...], ...],  // 16 rows × 20 cols, indices into palette
    "markers": [{"type":"spawn", "x":7, "y":6}, ...]
  }

Tileset key format: "bank_hex:gfx_step_decimal"
  e.g., "30:12" = bank $30, GFX step_id 12 (decimal) = $0C (hex)

Usage:
  python3 tools/build_combined_tileset.py export.json
  python3 tools/build_combined_tileset.py export.json --out-dir patches/
  python3 tools/build_combined_tileset.py export.json --info  # just show stats

Generated by: tools/build_combined_tileset.py
"""
import json, os, sys, argparse
from collections import OrderedDict

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
ROOT_DIR = os.path.join(SCRIPT_DIR, '..')
ROM_PATH = os.path.join(ROOT_DIR, 'data', 'DWM-original.gbc')

sys.path.insert(0, ROOT_DIR)
from tools.compress_tiles import compress_lz
from tools.decompress_tiles import decompress_lz


# ---------------------------------------------------------------------------
# Tileset key parsing
# ---------------------------------------------------------------------------

def parse_ts_key(ts_key):
    """Parse editor tileset key "bank_hex:gfx_step_decimal" → (bank, step_id).

    The editor encodes keys as bank in hex, GFX step_id in decimal.
    e.g., "30:12" → bank=0x30, step=12(dec)=0x0C(hex)
    """
    parts = ts_key.split(':')
    bank = int(parts[0], 16)
    gfx_step = int(parts[1])  # decimal, NOT hex
    return bank, gfx_step


# ---------------------------------------------------------------------------
# 2bpp tile extraction
# ---------------------------------------------------------------------------

def extract_tile_gfx(rom, bank, gfx_step, tile_idx):
    """Extract 16 bytes of 2bpp pixel data for a single 8×8 tile.

    Args:
        rom: full ROM bytes
        bank: tileset bank number
        gfx_step: GFX step_id (from $26DD table, passed to DecompressTileLayout)
        tile_idx: tile index within the tileset (0-127)

    Returns:
        bytes: 16 bytes of 2bpp tile data
    """
    result = decompress_lz(rom, bank, gfx_step)
    if result is None:
        raise ValueError(f"Failed to decompress tileset bank ${bank:02X} step ${gfx_step:02X}")
    data, _ = result
    if len(data) != 2048:
        raise ValueError(f"Expected 2048 bytes, got {len(data)} from bank ${bank:02X} step ${gfx_step:02X}")
    offset = tile_idx * 16
    return data[offset:offset + 16]


