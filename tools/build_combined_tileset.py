def main():
    parser = argparse.ArgumentParser(description='Build combined tileset from editor export')
    parser.add_argument('export_json', help='Editor export JSON file')
    parser.add_argument('--out-dir', default=None,
                        help='Output directory (default: patches/)')
    parser.add_argument('--info', action='store_true',
                        help='Show stats only, don\'t generate files')
    parser.add_argument('--rom', default=ROM_PATH,
                        help='Path to DWM-original.gbc')
    args = parser.parse_args()

    # Load export
    with open(args.export_json) as f:
        export_data = json.load(f)

    # Load ROM
    with open(args.rom, 'rb') as f:
        rom = f.read()

    palette = export_data['palette']
    layout = export_data['layout']
    markers = export_data.get('markers', [])

    print(f"Export: {len(palette)} tiles from {len(set(e['ts'] for e in palette))} tilesets",
          file=sys.stderr)
    print(f"Layout: {len(layout)}×{len(layout[0]) if layout else 0}", file=sys.stderr)

    # Build everything
    result = build_combined(export_data, rom)

    print(f"Combined tileset: {result['tile_count']} tiles", file=sys.stderr)
    print(f"Walkable: {result['threshold']} / Wall: {result['tile_count'] - result['threshold']}",
          file=sys.stderr)
    print(f"Collision threshold: {result['threshold']}", file=sys.stderr)
    print(f"Source tilesets: {', '.join(sorted(result['source_tilesets']))}", file=sys.stderr)

    if args.info:
        return

    # Compress
    from tools.tile_layout_compiler import pad_layout
    layout_padded = pad_layout(result['layout_20x16'])
    layout_compressed = compress_lz(layout_padded)
    attr_compressed = compress_lz(result['attr_256'])
    gfx_compressed = compress_lz(result['gfx_bytes'])

    print(f"\nCompression:", file=sys.stderr)
    print(f"  GFX:    2048 → {len(gfx_compressed)} bytes ({len(gfx_compressed)/2048*100:.0f}%)",
          file=sys.stderr)
    print(f"  Layout: 512 → {len(layout_compressed)} bytes ({len(layout_compressed)/512*100:.0f}%)",
          file=sys.stderr)
    print(f"  Attr:   256 → {len(attr_compressed)} bytes ({len(attr_compressed)/256*100:.0f}%)",
          file=sys.stderr)

    # Determine output directory
    out_dir = args.out_dir or os.path.join(ROOT_DIR, 'patches')
    os.makedirs(out_dir, exist_ok=True)

    # Generate ASM files
    bank067_asm = generate_bank_067(gfx_compressed)
    bank064_asm = generate_bank_064(layout_compressed, attr_compressed)

    # Palette colors
    pal_inc = generate_palette_inc(result['palette_64'], result['palette_info'])

    # Write outputs
    bank067_path = os.path.join(out_dir, 'bank_067.asm')
    bank064_path = os.path.join(out_dir, 'bank_064.asm')
    pal_path = os.path.join(out_dir, 'palette_6b.inc')

    with open(bank067_path, 'w') as f:
        f.write(bank067_asm)
    print(f"\nWritten: {bank067_path}", file=sys.stderr)

    with open(bank064_path, 'w') as f:
        f.write(bank064_asm)
    print(f"Written: {bank064_path}", file=sys.stderr)

    with open(pal_path, 'w') as f:
        f.write(pal_inc)
    print(f"Written: {pal_path}", file=sys.stderr)

    # Show $2A35 entry that needs to be written
    print(f"\n=== ROM0 $2A35 entry (mapID $6B in $26DD table) ===", file=sys.stderr)
    print(f"  Byte 0 (step_id): $00", file=sys.stderr)
    print(f"  Byte 1 (bank):    $67", file=sys.stderr)
    print(f"  Byte 6 (threshold): ${result['threshold']:02X} ({result['threshold']})", file=sys.stderr)
    entry = bytearray([0x00, 0x67, 0xA0, 0x00, 0x80, 0x00, result['threshold'], 0x00])
    print(f"  Full entry: {' '.join(f'${b:02X}' for b in entry)}", file=sys.stderr)

    # Show markers
    if markers:
        print(f"\n=== Markers ===", file=sys.stderr)
        for m in markers:
            mtype = m.get('type', '?')
            x, y = m.get('x', 0), m.get('y', 0)
            if mtype == 'exit':
                print(f"  Exit at ({x},{y}) — exit coords = ({x//2},{y//2})", file=sys.stderr)
            else:
                print(f"  {mtype} at ({x},{y})", file=sys.stderr)


if __name__ == '__main__':
    main()
