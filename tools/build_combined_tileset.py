def format_asm_db(data, label, comment="", bytes_per_line=16):
    """Format compressed data as ASM db statements."""
    lines = []
    if comment:
        lines.append(f'; {comment}')
    lines.append(f'{label}:')
    for i in range(0, len(data), bytes_per_line):
        chunk = data[i:i + bytes_per_line]
        hex_bytes = ', '.join(f'${b:02X}' for b in chunk)
        lines.append(f'    db {hex_bytes}')
    return '\n'.join(lines)


def generate_bank_067(gfx_compressed):
    """Generate bank_067.asm with combined GFX data."""
    lines = [
        '; =============================================================================',
        '; BANK $67 — Combined Tileset GFX (multi-tileset mashup)',
        '; =============================================================================',
        '; Generated by tools/build_combined_tileset.py',
        '; Format: pointer table at $4001, LZSS compressed 2bpp tile data',
        '; Entry 0: combined tileset GFX (2048 bytes decompressed = 128 tiles × 16 bytes)',
        '; Loaded by DecompressTileLayout via $26DD table entry for mapID $6B',
        '; =============================================================================',
        '',
        'SECTION "ROM Bank $067", ROMX[$4000], BANK[$67]',
        '    db $67  ; bank self-ID',
        '',
        '; Pointer table at $4001',
        '    dw CombinedGFX_Room6B',
        '',
        format_asm_db(gfx_compressed, 'CombinedGFX_Room6B',
                      f'CombinedGFX_Room6B: {len(gfx_compressed)} bytes compressed (2048 decompressed)'),
        '',
        '; Remaining bank space available for additional combined tilesets',
        '',
    ]
    return '\n'.join(lines)


def generate_bank_064(layout_compressed, attr_compressed, layout_json_name="export"):
    """Generate bank_064.asm with layout + attr data."""
    lines = [
        '; =============================================================================',
        '; BANK $64 — Custom Tile Layout + Attribute Data',
        '; =============================================================================',
        '; Layout generated by tools/build_combined_tileset.py',
        '; Attr data generated by tools/build_combined_tileset.py',
        '; Format: pointer table at $4001, LZSS compressed data',
        '; Entry 0: tile layout (512 bytes decompressed, 32×16 padded grid)',
        '; Entry 1: palette attributes (256 bytes decompressed, nibble-packed)',
        '; Engine reads via DecompressTileLayout ($1627): D=bank, E=entry index',
        '; =============================================================================',
        '',
        'SECTION "ROM Bank $064", ROMX[$4000], BANK[$64]',
        '    db $64  ; bank self-ID',
        '',
        '; Pointer table at $4001 — indexed by entry number (E register)',
        '; Entry 0: tile layout data (used by room step entry byte 0)',
        '; Entry 1: palette attribute data (used by bank $17 CustomAttrCheck)',
        '    dw CustomLayout_Room6B',
        '    dw CustomAttr_Room6B',
        '',
        format_asm_db(layout_compressed, 'CustomLayout_Room6B',
                      f'CustomLayout_Room6B: {len(layout_compressed)} bytes compressed (512 decompressed)'),
        '',
        format_asm_db(attr_compressed, 'CustomAttr_Room6B',
                      f'CustomAttr_Room6B: {len(attr_compressed)} bytes compressed (256 decompressed)'),
        '',
        '; Remaining bank space available for additional layouts/attrs',
        '',
    ]
    return '\n'.join(lines)


def generate_palette_inc(palette_64, info):
    """Generate palette color data as ASM db statements for bank $17."""
    lines = [
        '; CustomPaletteColors_6B: 64 bytes (8 palettes × 4 colors × 2 bytes)',
        '; Generated by tools/build_combined_tileset.py',
        '; Format: RGB15 LE pairs — [lo, hi] per color, 4 colors per palette',
    ]
    for slot, source in sorted(info.items()):
        lines.append(f';   Palette {slot}: from {source}')
    lines.append('CustomPaletteColors_6B:')
    for pal in range(8):
        chunk = palette_64[pal * 8:(pal + 1) * 8]
        hex_bytes = ', '.join(f'${b:02X}' for b in chunk)
        lines.append(f'    db {hex_bytes}  ; palette {pal}')
    return '\n'.join(lines)


