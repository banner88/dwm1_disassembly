def _palette_asm(prj, pal):
    out = [f"{pal['label']}:"]
    rows = pal['colors_rgb555']
    cmts = pal.get('row_comments') or [None] * 8
    for row, cm in zip(rows, cmts):
        b = F.palette_row(row)
        out.append(F.db_line(b, comment=cm))
    return out


def emit_region_palettes_a(prj, warnings):
    out = []
    for pal in prj.palettes:
        if pal.get('placement', 'b') == 'a':
            if pal.get('comment'):
                out += [f"; {c}" for c in pal['comment']]
            out += _palette_asm(prj, pal)
            out.append("")
    return "\n".join(out).rstrip('\n') + ("\n" if out else "")


def emit_region_render_tables(prj, warnings):
    out = ["; Per-custom-room render tables (generated by build_project.py).",
           "; Indexed (wMapID - $6B) by CustomPalCheck / CustomAttrCheck",
           "; (patches/bank_017.asm engine code). ptr $0000 / bank $00 =",
           "; borrow the vanilla source-map palette/attr. One row per room.",
           "CustomRoomPalPtr:"]
    for r in prj.rooms:
        pal = prj.room_palette(r)
        tgt = pal['label'] if pal else "$0000"
        why = pal['id'] if pal else "borrow vanilla palette"
        out.append(f"    dw {tgt}    ; {F.hexb(F.val(r['mapID']))} — {why}")
    out.append("")
    out.append("CustomRoomAttr:")
    for r in prj.rooms:
        at = (r.get('render') or {}).get('attr')
        if at:
            b = [F.val(at['bank']), F.val(at['base_entry'])]
            cm = (f"{F.hexb(F.val(r['mapID']))} -> bank "
                  f"{F.hexb(F.val(at['bank']))}, attr base entry "
                  f"{F.val(at['base_entry'])}")
        else:
            b, cm = [0, 0], (f"{F.hexb(F.val(r['mapID']))} — vanilla attr "
                             "fallback")
        out.append(F.db_line(b, comment=cm))
    out.append("")
    for pal in prj.palettes:
        if pal.get('placement', 'b') == 'b':
            if pal.get('comment'):
                out += [f"; {c}" for c in pal['comment']]
            out += _palette_asm(prj, pal)
            out.append("")
    return "\n".join(out).rstrip('\n') + "\n"


