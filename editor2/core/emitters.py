# bank $60 emitter — rooms + scripts + text (owns file patches/bank_060.asm)
# ---------------------------------------------------------------------------

def emit_bank_060(prj, warnings):
    rooms = prj.rooms
    lines = [template('bank_060_head.asm').rstrip('\n'), ""]

    # ---- script area ------------------------------------------------------
    lines += banner("SCRIPT DATA (generated by build_project.py)", [
        "CRITICAL: Index 0 = room entry script (runs on scroll/reload).",
        "          NPC scripts start at index 1+ (KEY_LESSONS Session 2).",
        "Master table width == compat list (legacy) or ALL rooms (default;",
        "fixes the S53 master-table overshoot for scroll in rooms >= index",
        "len(master)).  See PROJECT_COMPILER.md §scripts."])
    master_rooms = prj.master_rooms()          # list of rooms (or None = noop)
    lines.append("CustomScriptMasterTable:")
    need_noop = False
    for r in master_rooms:
        if r is None or not r.get('scripts'):
            lines.append("    dw CustomScriptNoop_PtrTable"
                         "  ; scriptless/placeholder room — safe no-op")
            need_noop = True
        else:
            lines.append(f"    dw {room_tag(r)}_ScriptPtrTable"
                         f"   ; mapID {F.hexb(F.val(r['mapID']))}")
    lines.append("")
    if need_noop:
        lines += ["CustomScriptNoop_PtrTable:",
                  "    dw CustomScriptNoop_Entry   ; [0] room entry (no-op)",
                  "CustomScriptNoop_Entry:",
                  "    dw $FFFF", ""]

    text_names = prj.text_comments()
    for r in rooms:
        if r.get('placeholder') or r.get('scripts_placement') == 'inline':
            continue
        lines += _room_scripts(prj, r, text_names, warnings)

    # ---- text area --------------------------------------------------------
    lines += banner("TEXT DATA — two-level pointer table (generated)", [
        "SaveBankAndSwitch ($00:$0940) indexes table[$C822*2] -> section,",
        "section[$C823*2] -> string (TEXT_SYSTEM.md). Flat tables crash.",
        "Custom ids $0A00+: section = hi-$0A, entry = lo",
        "(bank $04 TextQueueCheck_Ext)."])
    sections = prj.text_sections()
    lines.append("CustomTextPtrTable:")
    for si in range(len(sections)):
        lines.append(f"    dw CustomTextSection{si}")
    lines.append("")
    for si, sec in enumerate(sections):
        lines.append(f"CustomTextSection{si}:")
        for tid, entry in sec:
            lines.append(f"    dw {prj.text_label(tid)}"
                         f"   ; {F.hexw(tid)}: {entry.get('comment','')}")
        lines.append("")
        for tid, entry in sec:
            cm = f"{F.hexw(tid)} — {entry.get('comment','')}".rstrip(' —')
            lines += T.render_entry_asm(prj.text_label(tid), entry, comment=cm)
            lines.append("")

    # ---- room data area ---------------------------------------------------
    lines += banner("ROOM DATA (generated)")
    lines.append("CustomSourceMapTable:")
    for r in rooms:
        lines.append(f"    db {F.hexb(F.val(r['source_mapID']))}"
                     f"   ; {F.hexb(F.val(r['mapID']))} — {r.get('id','')}")
    lines.append("")
    lines.append("CustomRoomPtrTable:")
    for r in rooms:
        tgt = ("CustomRoomDummy_SubTable" if r.get('placeholder')
               else f"{room_tag(r)}_SubTable")
        lines.append(f"    dw {tgt}   ; {F.hexb(F.val(r['mapID']))}")
    lines.append("")

    dummy_emitted = False
    for r in rooms:
        if r.get('placeholder'):
            if not dummy_emitted:
                lines += banner("Dummy subtable — placeholder mapIDs "
                                "(never entered; all screens invalid)")
                lines.append("CustomRoomDummy_SubTable:")
                lines.append("    dw $FFFF, $FFFF, $FFFF, $FFFF,"
                             " $FFFF, $FFFF, $FFFF, $FFFF")
                lines.append("")
                dummy_emitted = True
            continue
        if r.get('scripts_placement') == 'inline':
            lines += _room_scripts(prj, r, text_names, warnings)
        lines += _room_data(prj, r)

    return "\n".join(lines) + "\n"


def _room_scripts(prj, r, text_names, warnings):
    tag = room_tag(r)
    out = [f"; --- {F.hexb(F.val(r['mapID']))} ({r.get('id','')}) scripts ---",
           f"{tag}_ScriptPtrTable:"]
    table = prj.room_script_table(r)          # ordered list of (idx, script_id)
    for idx, sid in table:
        out.append(f"    dw {tag}_Scr{idx:02d}   ; [{idx}] {sid}")
    out.append("")
    for idx, sid in table:
        script = prj.script(sid)
        out += S.emit_script(f"{tag}_Scr{idx:02d}", script['ops'],
                             text_names=text_names, warnings=warnings)
        out.append("")
    return out


def _room_data(prj, r):
    tag = room_tag(r)
    out = [f"; --- {F.hexb(F.val(r['mapID']))} ({r.get('id','')}) room data ---",
           f"{tag}_SubTable:"]
    screens = prj.room_screens(r)             # dict idx -> screen
    width = prj.subtable_width(r)
    row = []
    for i in range(width):
        row.append(f"{tag}_Screen{i}" if i in screens else "$FFFF")
    # match hand-authored style: one dw per valid screen, $FFFF runs grouped
    i = 0
    while i < width:
        if i in screens:
            out.append(f"    dw {tag}_Screen{i}")
            i += 1
        else:
            j = i
            while j < width and j not in screens:
                j += 1
            out.append("    dw " + ", ".join(["$FFFF"] * (j - i)))
            i = j
    out.append("")
    for i in sorted(screens):
        s = screens[i]
        ctr = prj.step_counter_label(r, i, s)
        lay = s['layout']
        out.append(f"{tag}_Screen{i}:")
        out.append(f"    dw {ctr}    ; step counter")
        out.append(f"    db {F.val(lay['entry'])}, {F.hexb(F.val(lay['bank']))}"
                   f"   ; step_id, tileset_bank")
        out.append(f"    dw {tag}_S{i}_NPCs")
        out.append(f"    dw {tag}_S{i}_Exits")
        out.append("")
        out.append(f"{tag}_S{i}_NPCs:")
        for n in s.get('npcs', []):
            if n['kind'] == 'spawn':
                b = F.npc_spawn_entry(n['x'], n['y'],
                                      F.val(n.get('script', 0)))
                out.append(F.db_line(b, comment=f"spawn ({n['x']},{n['y']})"
                           + (f" — {n['comment']}" if n.get('comment') else "")))
            else:
                sid = n['script']
                sidx = (0xFF if sid in (None, 'none')
                        else prj.script_index(r, sid))
                b = F.npc_entry(n.get('facing', 'down'), F.val(n['sprite']),
                                n['x'], n['y'], sidx)
                out.append(F.db_line(
                    b, comment=f"NPC ({n['x']},{n['y']}) script "
                               f"{sid if sid not in (None,'none') else 'none'}"))
        out.append("    db $FF")
        out.append("")
        out.append(f"{tag}_S{i}_Exits:")
        for e in s.get('exits', []):
            dest = prj.resolve_dest(e['dest'])
            b = F.exit_entry(e['x'], e['y'], dest,
                             F.val(e.get('gate_flag', 0)),
                             F.val(e['screen_byte']),
                             e['spawn_x'], e['spawn_y'])
            out.append(F.db_line(b, comment=e.get('comment',
                       f"exit ({e['x']},{e['y']}) -> {e['dest']}")))
        out.append("    db $FF")
        out.append("")
    return out


